


fn movement(command: &str, x: i32, y: i32) -> (i32, i32) {
    let sl = command.split_whitespace().collect::<Vec<&str>>();
    let c: i32 = sl[1].parse().unwrap();
    match sl[0] {
        "down" => { (x, y+c) },
        "forward" => { (x+c, y) },
        "up" => { (x, y-c) },
        _ => { panic!("why am I here?") }
    }
}


fn calculate_pos(data: &str) -> i32 {
    let res = data.lines()
        .fold((0, 0), |(x, y), s| movement(s, x, y));

    res.0 * res.1
}


fn movement_with_aim(command: &str, x: i64, y:i64, aim:i64) -> (i64, i64, i64) {
    let sl = command.split_whitespace().collect::<Vec<&str>>();
    let c: i64 = sl[1].parse().unwrap();

    match sl[0] {
        "down" => { (x, y, aim+c) },
        "forward" => { (x+c, y+(c*aim), aim) },
        "up" => { (x, y, aim-c) },
        _ => { panic!("why am I here?") }
    }
}


fn calculate_pos_aim(data: &str) -> i64 {
    let res = data.lines()
        .fold((0, 0, 0), |(x, y, aim), s| movement_with_aim(s, x, y, aim));

    res.0 * res.1
}


